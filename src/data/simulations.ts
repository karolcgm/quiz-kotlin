import type { Simulation } from "@/types/simulation";

type SimulationSeed = Omit<
  Simulation,
  "subjectId" | "ageGroup" | "difficulty" | "teacherHint"
> &
  Partial<Pick<Simulation, "subjectId" | "ageGroup" | "difficulty" | "teacherHint">>;

function inferAgeGroup(grades: Simulation["grades"]): Simulation["ageGroup"] {
  const highestGrade = Math.max(...grades);
  if (highestGrade <= 3) return "early";
  if (highestGrade >= 8) return "exam";
  return "middle";
}

function inferDifficulty(grades: Simulation["grades"]): Simulation["difficulty"] {
  const highestGrade = Math.max(...grades);
  if (highestGrade <= 3) return "easy";
  if (highestGrade >= 7) return "advanced";
  return "medium";
}

function inferAssessmentSkills(seed: SimulationSeed): string[] {
  const source = [
    seed.slug,
    seed.title,
    seed.shortDescription,
    seed.visualKind,
    seed.interactionKind,
    ...(seed.tags ?? []),
  ]
    .join(" ")
    .toLowerCase();

  if (source.includes("odejm") || source.includes("różnic")) return ["subtraction"];
  if (source.includes("mnoż") || source.includes("tabliczk") || source.includes("potęg")) {
    return ["multiplication"];
  }
  if (source.includes("dziel") || source.includes("iloraz")) return ["division"];
  if (source.includes("ułam") || source.includes("dziesięt") || source.includes("procent")) {
    return ["fractions"];
  }
  if (
    source.includes("geometr") ||
    source.includes("figur") ||
    source.includes("pole") ||
    source.includes("obwód") ||
    source.includes("kąt") ||
    source.includes("trójkąt") ||
    source.includes("koło") ||
    source.includes("brył")
  ) {
    return ["geometry"];
  }
  if (
    source.includes("jednost") ||
    source.includes("długość") ||
    source.includes("masa") ||
    source.includes("czas") ||
    source.includes("skala") ||
    source.includes("prędkość") ||
    source.includes("droga")
  ) {
    return ["measurement"];
  }
  if (source.includes("algebra") || source.includes("równ") || source.includes("wyraż") || source.includes("funkcj")) {
    return ["algebra"];
  }
  if (
    source.includes("statyst") ||
    source.includes("diagram") ||
    source.includes("średnia") ||
    source.includes("mediana") ||
    source.includes("prawdopodob")
  ) {
    return ["statistics"];
  }

  return ["addition"];
}

function simulation(seed: SimulationSeed): Simulation {
  return {
    subjectId: "math",
    ageGroup: inferAgeGroup(seed.grades),
    difficulty: inferDifficulty(seed.grades),
    teacherHint: `Ustaw przykład i poproś uczniów, aby przewidzieli wynik przed zmianą wartości.`,
    ...seed,
    status: seed.status ?? "planned",
    assessmentReady: seed.assessmentReady ?? false,
    assessmentSkills: seed.assessmentSkills ?? inferAssessmentSkills(seed),
  };
}

